let button0 = document.querySelector('.scroll1');
let sectionFirst = document.querySelector('.tabs-tab');

function scrollTo(element) {
    window.scroll({
        left: 0,
        top: element.offsetTop,
        behavior: 'smooth',
    })
}
if (button0 && sectionFirst) {
    button0.addEventListener('click', () => {
        scrollTo(sectionFirst);
    })
}


